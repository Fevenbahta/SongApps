import mongoose from 'mongoose';

const SongSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var SongMessage = mongoose.model('SongMessage', SongSchema);

export default SongMessage;